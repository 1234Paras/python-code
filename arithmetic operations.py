'''Program to read two integers from keyboard and
perform various arithmetic operations on it and
print the result of these operations'''

a=int(input("Enter a first number"))
b=int(input("Enter a second number"))

#-------Addition------
result=a+b
print("Sum is = ",result)
#------Subtraction------
result=a-b
print("Subtraction is = ",result)
#------Multiplication-----
result=a*b
print("Multilplication is = ",result)
#------Division-----------
result=a/b
print("Division is = ",result)
#-----Remainder-----------
result=a%b
print("Remainder is = ",result)
